"""
Классификация темы сообщения: ``support`` | ``health`` | ``food``.

v1 — по ключевым словам. Матчинг — по ГРАНИЦЕ СЛОВА (стем в начале слова), а не
наивной подстрокой: иначе «вет» ловится в «при-вет», «от-вет», «со-вет».
Многословные ключи (с пробелом) ищем обычной подстрокой.
"""

import re

# Мета-вопросы про самого ассистента/сервис — всегда support, даже если внутри
# есть слово «ветеринар»/«корм» («заменяешь ли ты ветеринара», «что ты умеешь»).
# Проверяются ПЕРВЫМИ. Список держим узким, чтобы не перехватывать реальные
# вопросы про питомца.
SUPPORT_OVERRIDE = (
    'заменяешь', 'кто ты', 'что ты уме', 'что умеешь', 'ты умеешь', 'чем ты помога',
    'чем можешь помочь', 'как с тобой', 'ты бот', 'ты человек',
)

# Порядок важен: «питание» проверяем раньше «здоровья», чтобы «вес корма» уходил
# в food, а «лишний вес питомца» — в health.
FOOD_KEYWORDS = (
    'корм', 'рацион', 'питан', 'кормить', 'кормлен', 'покорм', 'еда', 'грамм',
    'калори', 'бренд', 'сухой', 'влажный', 'лакомств', 'миск', 'порци', 'меню',
)

HEALTH_KEYWORDS = (
    'здоров', 'болезн', 'болеет', 'симптом', 'прививк', 'вакцин', 'аллерг',
    'рвот', 'понос', 'диаре', 'температур', 'чеш', 'зуд', 'шерст', 'линя',
    'хрома', 'худе', 'ожирен', 'лишний вес', 'ветеринар', 'ветклиник', 'ветврач',
    'лекарств', 'таблетк', 'глист', 'клещ', 'блох', 'кашл', 'лапа',
)


def _matches(text: str, keywords) -> bool:
    for kw in keywords:
        if ' ' in kw:
            if kw in text:
                return True
        elif re.search(r'(?<![а-яёa-z0-9])' + re.escape(kw), text):
            return True
    return False


class RouterService:
    """Определение способности по тексту запроса."""

    @staticmethod
    def classify(message: str, pet_id=None) -> str:
        text = (message or '').lower()
        if _matches(text, SUPPORT_OVERRIDE):
            return 'support'
        if _matches(text, FOOD_KEYWORDS):
            return 'food'
        if _matches(text, HEALTH_KEYWORDS):
            return 'health'
        return 'support'
